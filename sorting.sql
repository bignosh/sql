SELECT MODEL, MANUFACTURER, YEAR FROM Cars1
ORDER BY YEAR ASC, MANUFACTURER DESC;
SELECT COUNT(MODEL) AS 'Number of Models', MANUFACTURER FROM Cars1
GROUP BY MANUFACTURER ORDER BY YEAR;
SELECT MANUFACTURER, AVG(HP) FROM Cars1
GROUP BY MANUFACTURER;
SELECT MANUFACTURER, COUNT(MODEL) FROM Cars1
GROUP BY MANUFACTURER HAVING COUNT(MODEL) > 2;
SELECT COUNT(MODEL), MANUFACTURER FROM Cars1
WHERE MANUFACTURER LIKE 'M%' GROUP BY MANUFACTURER;
